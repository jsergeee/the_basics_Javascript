// Функция для транслитерации строки
function transliterate($string)
{
    global $transliterationMap; // Используем глобальный массив
    $transliteratedString = '';

    // Проходим по каждому символу строки
    for ($i = 0; $i < mb_strlen($string, 'UTF-8'); $i++) {
        $char = mb_substr($string, $i, 1, 'UTF-8'); // Получаем текущий символ

        // Если символ есть в массиве, заменяем его, иначе оставляем как есть
        if (isset($transliterationMap[$char])) {
            $transliteratedString .= $transliterationMap[$char];
        } else {
            $transliteratedString .= $char; // Оставляем символ без изменений
        }
    }

    return $transliteratedString; // Возвращаем транслитерированную строку
}

