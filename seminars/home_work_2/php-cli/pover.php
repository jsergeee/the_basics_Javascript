<?php
// Функция для возведения числа в степень с использованием рекурсии
function power($val, $pow)
{
    // Если степень равна 0, возвращаем 1 (по определению)
    if ($pow == 0) {
        return 1;
    }
    // Если степень отрицательная, изменяем знак степени и рекурсивно вызываем функцию
    if ($pow < 0) {
        return 1 / power($val, -$pow);
    }
    // Рекурсивный вызов функции
    return $val * power($val, $pow - 1);
}

// Примеры использования функции
echo power(2, 3) . "\n";  // 8 (2^3)
echo power(5, 0) . "\n";  // 1 (5^0)
echo power(3, 2) . "\n";  // 9 (3^2)
echo power(2, -3) . "\n"; // 0.125 (2^(-3))
